export const getFontFamily = (baseFont = 'Inter', weight) => {
  switch (weight) {
    case '100':
      return `${baseFont}-Thin`;
    case '200':
      return `${baseFont}-Thin`;
    case '300':
      return `${baseFont}-Thin`;
    case '400':
      return `${baseFont}-Thin`;
    case '500':
      return `${baseFont}-Thin`;
    case '600':
      return `${baseFont}-Thin`;
    case '700':
      return `${baseFont}-Thin`;
    case '800':
      return `${baseFont}-Thin`;
    case '900':
      return `${baseFont}-Thin`;
  }
};
